    private void initJComboBoxEstado(Map<String, String> estados) {
        jComboBoxEstado.removeAllItems();
        
        for(String uf : estados.keySet()) {
            String estado = estados.get(uf);
            jComboBoxEstado.addItem(estado);
        }
    }
    
